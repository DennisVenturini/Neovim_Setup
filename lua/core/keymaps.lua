local function map(mode, lhs, rhs, desc)
	vim.keymap.set(mode, lhs, rhs, { noremap = true, silent = true, desc = desc })
end
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Variable for reuse
local opts = { noremap = true, silent = true }
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Set <space> as the leader key
-- See `:help mapleader`
--  NOTE: Must happen before plugins are loaded (otherwise wrong leader will be used)
vim.g.mapleader = " "
vim.g.maplocalleader = " "
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- fd for esc
vim.keymap.set({ "i", "v", "n", "s", "x", "o", "c" }, "fd", "<Esc>", { noremap = true, silent = true })
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Disable the Spacebar key's default behavior in normal and visual modes
vim.keymap.set({ "n", "v" }, "<Space>", "<Nop>", { silent = true })
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- save file
map("n", "<C-s>", "<cmd> w <CR>", "[S]ave File")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- save file without auto-formatting
map("n", "<leader>sn", "<cmd>noautocmd w <CR>", "save file without auto-formatting")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- quit file
map("n", "<C-q>", "<cmd> q <CR>", "quit file")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- delete single character without copying into register
map("n", "x", '"_x', "delete single character without copying into register")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Vertical scroll and center
map("n", "<C-d>", "<C-d>zz", "Vertical scroll and center")
map("n", "<C-u>", "<C-u>zz", "Vertical scroll and center")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Find and center
map("n", "n", "nzzzv", "Find and center")
map("n", "N", "Nzzzv", "Find and center")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Resize with arrows
map("n", "<Up>", ":resize -2<CR>", "Resize with arrows")
map("n", "<Down>", ":resize +2<CR>", "Resize with arrows")
map("n", "<Left>", ":vertical resize -2<CR>", "Resize with arrows")
map("n", "<Right>", ":vertical resize +2<CR>", "Resize with arrows")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Buffers
map("n", "<Tab>", ":bnext<CR>", "Buffers: Next Buffer")
map("n", "<S-Tab>", ":bprevious<CR>", "Buffers: Previous Buffer")
map("n", "<leader>x", ":bdelete!<CR>", "Buffers: close buffer") -- close buffer
map("n", "<leader>b", "<cmd> enew <CR>", "Buffers: new buffer") -- new buffer
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Window management
map("n", "<leader>v", "<C-w>v", "Window management: split window vertically") -- split window vertically
map("n", "<leader>h", "<C-w>s", "Window management: split window horizontally") -- split window horizontally
map("n", "<leader>se", "<C-w>=", "Window management: make split windows equal width & height") -- make split windows equal width & height
map("n", "<leader>xs", ":close<CR>", "Window management: close current split window") -- close current split window
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Navigate between splits
map("n", "<C-k>", ":wincmd k<CR>", "Navigate between splits")
map("n", "<C-j>", ":wincmd j<CR>", "Navigate between splits")
map("n", "<C-h>", ":wincmd h<CR>", "Navigate between splits")
map("n", "<C-l>", ":wincmd l<CR>", "Navigate between splits")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Tabs
map("n", "<leader>to", ":tabnew<CR>", "Tabs: open new tab") -- open new tab
map("n", "<leader>tx", ":tabclose<CR>", "Tabs: close current tab") -- close current tab
map("n", "<leader>tn", ":tabn<CR>", "Tabs: go to next tab") --  go to next tab
map("n", "<leader>tp", ":tabp<CR>", "Tabs: go to previous tab") --  go to previous tab
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Toggle line wrapping
map("n", "<leader>lw", "<cmd>set wrap!<CR>", "Toggle line wrapping")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Stay in indent mode
map("v", "<", "<gv", "Stay in indent mode")
map("v", ">", ">gv", "Stay in indent mode")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Keep last yanked when pasting
map("v", "p", '"_dP', "Keep last yanked when pasting")
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Diagnostic keymaps
vim.keymap.set("n", "[d", function()
	vim.diagnostic.jump({ count = -1, float = true })
end, { desc = "Go to previous diagnostic message" })

vim.keymap.set("n", "]d", function()
	vim.diagnostic.jump({ count = 1, float = true })
end, { desc = "Go to next diagnostic message" })

vim.keymap.set("n", "<leader>d", vim.diagnostic.open_float, { desc = "Open floating diagnostic message" })
vim.keymap.set("n", "<leader>q", vim.diagnostic.setloclist, { desc = "Open diagnostics list" })
